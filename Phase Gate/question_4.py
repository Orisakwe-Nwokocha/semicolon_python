def sum(x, y):
	return x + y

def division(x, y):
	return x / y

def subtraction(x, y):
	return x - y

def multiplication(x, y):
	return x * y

def floor_division(x, y):
	return x // y


print(sum(10, 20))
print(division(10, 20))
print(subtraction(10, 20))
print(multiplication(10, 20))
print(floor_division(10, 20))